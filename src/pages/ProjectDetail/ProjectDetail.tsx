import { useParams, Link, Navigate } from "react-router-dom";
import { getProjectById } from "../../data";
import { ImageGallery, PageTransition } from "../../components";
import "./ProjectDetail.css";

const ProjectDetail = () => {
    const { projectId } = useParams<{ projectId: string }>();
    const project = projectId ? getProjectById(projectId) : undefined;

    if (!project) {
        return <Navigate to="/work" replace />;
    }

    return (
        <article className="project-detail">
            <PageTransition>
                <Link to="/work" className="project-detail__back">
                    ‚Üê Back to work
                </Link>

                <header className="project-detail__header">
                    <div className="project-detail__meta">
                        {project.year && (
                            <span className="project-detail__year">
                                {project.year}
                            </span>
                        )}
                        <div className="project-detail__tags">
                            {project.tags.map((tag) => (
                                <span key={tag} className="project-detail__tag">
                                    {tag}
                                </span>
                            ))}
                        </div>
                    </div>
                    <h1 className="project-detail__title">{project.title}</h1>
                </header>

                <div className="project-detail__links">
                    {project.link && (
                        <a
                            href={project.link}
                            target="_blank"
                            rel="noopener noreferrer"
                            className="project-detail__link"
                        >
                            <svg
                                width="16"
                                height="16"
                                viewBox="0 0 24 24"
                                fill="none"
                                stroke="currentColor"
                                strokeWidth="1.5"
                            >
                                <path d="M18 13v6a2 2 0 0 1-2 2H5a2 2 0 0 1-2-2V8a2 2 0 0 1 2-2h6" />
                                <polyline points="15 3 21 3 21 9" />
                                <line x1="10" y1="14" x2="21" y2="3" />
                            </svg>
                            Live Site
                        </a>
                    )}
                    {project.github && (
                        <a
                            href={project.github}
                            target="_blank"
                            rel="noopener noreferrer"
                            className="project-detail__link"
                        >
                            <svg
                                width="16"
                                height="16"
                                viewBox="0 0 24 24"
                                fill="none"
                                stroke="currentColor"
                                strokeWidth="1.5"
                            >
                                <path d="M9 19c-5 1.5-5-2.5-7-3m14 6v-3.87a3.37 3.37 0 0 0-.94-2.61c3.14-.35 6.44-1.54 6.44-7A5.44 5.44 0 0 0 20 4.77 5.07 5.07 0 0 0 19.91 1S18.73.65 16 2.48a13.38 13.38 0 0 0-7 0C6.27.65 5.09 1 5.09 1A5.07 5.07 0 0 0 5 4.77a5.44 5.44 0 0 0-1.5 3.78c0 5.42 3.3 6.61 6.44 7A3.37 3.37 0 0 0 9 18.13V22" />
                            </svg>
                            Source Code
                        </a>
                    )}
                </div>

                <div className="project-detail__content">
                    {project.fullDescription ? (
                        project.fullDescription.map((paragraph, index) => (
                            <p key={index}>{paragraph}</p>
                        ))
                    ) : (
                        <p>{project.description}</p>
                    )}
                </div>

                {project.images && project.images.length > 0 && (
                    <div className="project-detail__gallery">
                        <ImageGallery images={project.images} alt={project.title} />
                    </div>
                )}
            </PageTransition>
        </article>
    );
};

export default ProjectDetail;

